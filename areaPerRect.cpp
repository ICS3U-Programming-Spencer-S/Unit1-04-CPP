// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 10cm X 4cm.
#include <iostream>

int main() {
    std::cout << "A rectangle has the dimensions of\n";
    std::cout << "length of 10cm and width of 4cm:\n";
    std::cout << "\n";
    std::cout << "Area = " << 10 * 4 << " cm^2\n";
    std::cout << "Perimeter = " << 2 * (10 + 4) << " cm\n";
}