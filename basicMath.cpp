#include <cmath>
#include <iostream>

int main() {
    std::cout << "5 + 3 = " << 5 + 3 << std::endl;
    std::cout << "9 - 6 = " << 9 - 6 << std::endl;
    std::cout << "6 / 3 = " << 6 / 3 << std::endl;
    std::cout << "6 - 3 / 2 = " << 6 - 3 / 2 << std::endl;
    std::cout << "6^2 = " << pow(6, 2) << std::endl;
    std::cout << "4 / 2 = " << float(4) / float(2) << std::endl;
}